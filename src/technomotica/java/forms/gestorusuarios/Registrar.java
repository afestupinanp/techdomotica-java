    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed

        String nombre1 = tfd_Nombre1.getText();
        String nombre2 = tfd_Nombre2.getText();
        String apellido1 = tfd_Apellido1.getText();
        String apellido2 = tfd_Apellido2.getText();
        String correo = tfd_correo.getText();
        String documento = tfd_documento.getText();
        String contra = tfd_contrase単a.getText();

        //Usuario person = new Usuario(nombre1, nombre2, apellido1, apellido2, correo, documento, contra);

        String str = String.format("Por favor, confirme los siguientes datos:\n\nPrimer nombre: %s\nSegundo nombre: %s.\nPrimer apellido: %s\nSegundo apellido: %s\nCorreo electr坦nico: %s\nDocumento de identidad: %s\nContrase単a: %s", nombre1, nombre2, apellido1, apellido2, correo, documento, contra);
        if(Util.chequearStrings(nombre1, nombre2, apellido1, apellido2, correo, documento, contra)) JOptionPane.showMessageDialog(null, "Uno de los campos de texto est叩n vac鱈os.", "Error", JOptionPane.ERROR_MESSAGE);
        else {
            int confirm = JOptionPane.showConfirmDialog(null, str, "Confirmaci坦n de datos", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(confirm == JOptionPane.YES_OPTION) {
                confirm = JOptionPane.showConfirmDialog(null, "多Deseas seguir agregando usuarios?", "Confirmaci坦n de datos", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if(confirm == JOptionPane.YES_OPTION) {
                    limpiar();
                }
                else redirigir();
            }
        }
        
    }//GEN-LAST:event_btn_registrarActionPerformed

